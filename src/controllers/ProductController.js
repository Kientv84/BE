const ProductService = require('../services/ProductService')

const createProduct = async (req, res) => {
    try {
        const { name, image, type, price, countInStock, rating, description, promotion, discount, image1, image2 } = req.body
        // console.log(req.body)
        // Kiểm tra nếu bất kỳ trường nào là chuỗi hoặc số âm
        if (
            typeof name !== 'string' ||
            typeof image !== 'string' ||
            typeof image1 !== 'string' ||
            typeof image2 !== 'string' ||
            typeof type !== 'string' ||
            typeof description !== 'string' ||
            typeof promotion !== 'string' ||
            typeof price !== 'number' || price <= 0 ||
            typeof countInStock !== 'number' || countInStock <= 0 ||
            typeof rating !== 'number' || rating < 0 || rating > 5 ||
            typeof discount !== 'number' || discount < 0) {
            return res.status(200).json({
                status: 'ERR',
                message: 'Invalid input. Please check your input values.'
            })
        } else if (!name || !image || !image1 || !image2 || !type || !price || !countInStock || !rating || !discount) {
            return res.status(200).json({
                status: 'ERR',
                message: 'The input is required'
            })
        }
        const response = await ProductService.createProduct(req.body)
        return res.status(200).json(response)
    } catch (e) {
        // console.log('error')
        return res.status(404).json({
            message: e
        })
    }
}

const updateProduct = async (req, res) => {
    try {
        const productId = req.params.id
        const data = req.body
        if (!productId) {
            return res.status(200).json({
                status: 'ERR',
                message: 'The product id is not exist'
            })
        }
        const response = await ProductService.updateProduct(productId, data)
        return res.status(200).json(response)
    } catch (e) {
        return res.status(404).json({
            message: e
        })
    }
}

